    private void telaToDisciplina() {
        disciplina.setCodigo(Integer.parseInt(tCodigo.getText()));
        disciplina.setCh(Integer.parseInt(tCh.getText()));
        disciplina.setNome(tNome.getText());
        disciplina.setProfessor(tProfessor.getText());
    }

    private void disciplinaToTela() {
        tCodigo.setText(Integer.toString(disciplina.getCodigo()));
        tCh.setText(Integer.toString(disciplina.getCh()));
        tNome.setText(disciplina.getNome());
        tProfessor.setText(disciplina.getProfessor());
    }

